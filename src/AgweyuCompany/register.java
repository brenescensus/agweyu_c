    private void SAVEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SAVEActionPerformed
        String email = emailtxt.getText();
        String firstname = firstnametxt.getText();
        String middlename = middlenametxt.getText();
        String lastname = lastnametxt.getText();
        String pass = password.getText();

        if (email.trim().isEmpty() || firstname.trim().isEmpty() || lastname.trim().isEmpty() || pass.trim().isEmpty()) {
            emaillable.setText("email is required");
            firstlable.setText("firstname is required");
            lastlable.setText("lastname is required");
            passlable.setText("password is required");
        } else if (email.trim().isEmpty()) {
            emaillable.setText("email is required");

        } else if (firstname.trim().isEmpty()) {
            firstlable.setText("email is required");

        } else if (lastname.trim().isEmpty()) {
            lastlable.setText("email is required");
        } else if (pass.trim().isEmpty()) {
            passlable.setText("password is required");
        } else if (!ValPassword(pass)) {

            JOptionPane.showMessageDialog(null, "the password should be atleast 8 characters with \n letters and numbers","Error", JOptionPane.ERROR_MESSAGE);

        } else if (ValidateEmail(email)) {

            if (!checkEmail(email)) {

                try {
                    String sql = "INSERT INTO `users` (`email`, `firstname`, `middlename`, `lastname`, `password`) "
                            + "VALUES ( '" + email + "', '" + firstname + "', '" + middlename + "', '" + lastname + "','" + pass + "')";
                    Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/agweyu_collins_company", "root", "");
                    Statement st = con.createStatement();
                    st.executeUpdate(sql);

                    JOptionPane.showMessageDialog(null, "you have been registered successfully");
                    setVisible(false);
                    new login().setVisible(true);

                } catch (SQLException e) {
                    JOptionPane.showMessageDialog(null, e);

                }
            }
        }
    }//GEN-LAST:event_SAVEActionPerformed

