    public boolean checkEmail(String email) {
        PreparedStatement pst;
        ResultSet rs;
        boolean email_exist = false;
        try {
            String query = "SELECT*FROM `users` WHERE email=?";
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/agweyu_collins_company", "root", "");
            pst = con.prepareStatement(query);
            pst.setString(1, email);
            rs = pst.executeQuery();

            if (rs.next()) {
                email_exist = true;
                JOptionPane.showMessageDialog(null, "The email used is already registerd", "Email Failed", 2);
            }
        } catch (Exception e) {
            System.out.println(e);
        }
        return email_exist;
    }

    public boolean ValidateEmail(String email) {
        boolean valid_email = true;
        if (!(Pattern.matches("^[a-zA-Z0-9]+[@]{1}+[a-zA-Z0-9]+[.]{1}+[a-zA-Z0-9]+$", email))) {
            valid_email = false;
            JOptionPane.showMessageDialog(null, "please enter a valid email address", "Error", JOptionPane.ERROR_MESSAGE);
        }

        return valid_email;
    }

    public static boolean ValPassword(String password) {
        if (password.length() > 7) {
            if (checkpass(password)) {
                return true;
            } else {
                return false;
            }
        } else {
            System.out.println("too small");
            return false;
        }
    }

    public static boolean checkpass(String password) {
        boolean hasNum = false;
        boolean hasCap = false;
        boolean hasLow = false;
        char c;
        for (int i = 0; i < password.length(); i++) {
            c = password.charAt(i);
            if (Character.isDigit(c)) {
                hasNum = true;
            } else if (Character.isUpperCase(c)) {
                hasCap = true;
            } else if (Character.isLowerCase(c)) {
                hasLow = true;
            }
            if (hasCap && hasNum && hasLow) {
                return true;
            }
        }

        return false;
    }

